#!/usr/bin/env python3
"""Render semantic frames for Replica scenes at NICE-SLAM camera poses.

Uses Habitat-sim to render the semantic sensor at each pose from traj.txt,
producing semantic/*.npy files that replace the all-zero placeholders
generated by niceslam_to_scovox.py.

Must be run in the Habitat conda environment:
    export PATH="$HOME/miniconda3/bin:$PATH"
    conda activate habitat
    python -m scovox_eval.replica_semantic_render --scene room0

Prerequisites:
    - Replica v1 dataset at REPLICA_V1_ROOT (with mesh_semantic.ply per scene)
    - NICE-SLAM dataset at NICESLAM_ROOT (with traj.txt per scene)
    - Habitat-sim installed in conda env

Output:
    Overwrites semantic/*.npy in the rendered dataset directory with real
    semantic instance IDs. Also writes semantic_class_map.json.
"""

from __future__ import annotations

import argparse
import json
import math
from pathlib import Path

import numpy as np

# Habitat-sim is only available in the conda 'habitat' env
import habitat_sim

# Scene name mapping: NICE-SLAM uses room0, Replica v1 uses room_0
NICESLAM_TO_REPLICA = {
    "room0": "room_0", "room1": "room_1", "room2": "room_2",
    "office0": "office_0", "office1": "office_1",
    "office2": "office_2", "office3": "office_3", "office4": "office_4",
}

SCENES = list(NICESLAM_TO_REPLICA.keys())

NICESLAM_ROOT = Path("/home/kalhan/Projects/datasets/Replica")
REPLICA_V1_ROOT = Path("/home/kalhan/Projects/datasets/replica_v1")
RENDERED_ROOT = Path("/home/kalhan/Projects/datasets/replica_rendered")


def find_semantic_mesh(scene_dir: Path) -> Path | None:
    """Find mesh_semantic.ply in a Replica v1 scene directory.

    Checks multiple possible locations because the layout varies depending
    on how the dataset was downloaded/extracted.
    """
    candidates = [
        scene_dir / "mesh_semantic.ply",
        scene_dir / "habitat" / "mesh_semantic.ply",
        scene_dir / "mesh_semantic.navmesh",
    ]
    for p in candidates:
        if p.exists() and p.suffix == ".ply":
            return p
    return None


def load_traj(path: Path) -> list:
    """Load NICE-SLAM trajectory: each line is a flattened 4×4 matrix."""
    matrices = []
    with open(path) as f:
        for line in f:
            vals = [float(v) for v in line.strip().split()]
            T = np.array(vals).reshape(4, 4)
            matrices.append(T)
    return matrices


def make_sim(scene_path: str, width: int, height: int, fx: float, fy: float):
    """Create Habitat-sim with semantic sensor matching NICE-SLAM camera."""
    backend_cfg = habitat_sim.SimulatorConfiguration()
    backend_cfg.scene_id = scene_path
    backend_cfg.enable_physics = False
    backend_cfg.gpu_device_id = 0

    # Compute hfov from fx and width
    hfov = 2.0 * math.atan(width / (2.0 * fx))
    hfov_deg = math.degrees(hfov)

    semantic_sensor = habitat_sim.CameraSensorSpec()
    semantic_sensor.uuid = "semantic"
    semantic_sensor.sensor_type = habitat_sim.SensorType.SEMANTIC
    semantic_sensor.resolution = [height, width]
    semantic_sensor.hfov = hfov_deg
    # Sensor at origin — we'll set the agent pose to the camera pose
    semantic_sensor.position = [0.0, 0.0, 0.0]

    agent_cfg = habitat_sim.agent.AgentConfiguration()
    agent_cfg.sensor_specifications = [semantic_sensor]

    cfg = habitat_sim.Configuration(backend_cfg, [agent_cfg])
    sim = habitat_sim.Simulator(cfg)
    return sim


def export_semantic_class_map(sim, output_path: Path):
    """Export instance ID → category name mapping."""
    scene = sim.semantic_scene
    class_map = {}
    for obj in scene.objects:
        if obj is not None:
            class_map[int(obj.semantic_id)] = {
                "category_name": obj.category.name() if obj.category else "unknown",
                "category_index": obj.category.index() if obj.category else -1,
            }
    with open(output_path, "w") as f:
        json.dump(class_map, f, indent=2)
    return class_map


def T_to_habitat_state(T_world_cam):
    """Convert a 4×4 camera-to-world matrix to Habitat agent state.

    NICE-SLAM stores camera-to-world in OpenGL convention (Y-up, -Z forward),
    which matches Habitat's convention for Replica scenes.  We extract
    position and rotation directly.
    """
    import quaternion as qt

    pos = T_world_cam[:3, 3]
    R = T_world_cam[:3, :3]
    quat = qt.from_rotation_matrix(R)

    state = habitat_sim.agent.AgentState()
    state.position = pos
    state.rotation = quat
    return state


def render_scene(scene_name: str, output_dir: Path, max_frames: int = 2000):
    """Render semantic frames for one scene."""
    replica_scene = NICESLAM_TO_REPLICA[scene_name]
    scene_dir = REPLICA_V1_ROOT / replica_scene
    scene_path = find_semantic_mesh(scene_dir)

    if scene_path is None:
        print(f"  ERROR: mesh_semantic.ply not found in {scene_dir}")
        print(f"  Looked in: {scene_dir}, {scene_dir / 'habitat'}")
        print(f"  Download Replica v1: bash Replica-Dataset/download.sh {REPLICA_V1_ROOT}")
        return False

    traj_path = NICESLAM_ROOT / scene_name / "traj.txt"
    if not traj_path.exists():
        print(f"  ERROR: Trajectory not found: {traj_path}")
        return False

    # Load NICE-SLAM camera params
    cam_params_path = NICESLAM_ROOT / "cam_params.json"
    with open(cam_params_path) as f:
        cam = json.load(f)["camera"]

    # Load trajectory
    traj = load_traj(traj_path)
    n_frames = min(len(traj), max_frames)

    # Create simulator
    print(f"  Loading scene: {scene_path}")
    sim = make_sim(str(scene_path), cam["w"], cam["h"], cam["fx"], cam["fy"])

    # Export class map
    sem_dir = output_dir / "semantic"
    sem_dir.mkdir(parents=True, exist_ok=True)
    class_map = export_semantic_class_map(sim, output_dir / "semantic_class_map.json")
    print(f"  {len(class_map)} semantic classes")

    # Render each frame
    agent = sim.get_agent(0)
    for i in range(n_frames):
        state = T_to_habitat_state(traj[i])
        agent.set_state(state, infer_sensor_states=True)

        obs = sim.get_sensor_observations()
        semantic = obs["semantic"].astype(np.int32)
        np.save(sem_dir / f"{i:06d}.npy", semantic)

        if i % 200 == 0:
            unique = np.unique(semantic)
            print(f"  Frame {i}/{n_frames} — {len(unique)} classes visible")

    sim.close()
    print(f"  Done: {n_frames} semantic frames → {sem_dir}")
    return True


def main():
    parser = argparse.ArgumentParser(
        description="Render semantic frames for Replica at NICE-SLAM poses"
    )
    parser.add_argument("--scene", choices=SCENES, default=None,
                        help="Single scene to render")
    parser.add_argument("--all", action="store_true",
                        help="Render all 8 scenes")
    parser.add_argument("--output-root", type=str, default=str(RENDERED_ROOT),
                        help="Root directory for rendered datasets")
    parser.add_argument("--max-frames", type=int, default=2000)
    args = parser.parse_args()

    output_root = Path(args.output_root)

    if args.all:
        scenes = SCENES
    elif args.scene:
        scenes = [args.scene]
    else:
        parser.error("Specify --scene or --all")

    for scene in scenes:
        print(f"\n=== {scene} ===")
        out_dir = output_root / scene
        render_scene(scene, out_dir, args.max_frames)


if __name__ == "__main__":
    main()
